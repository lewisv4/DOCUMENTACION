    /* SubConsulta de lista */

SELECT PRECIO FROM PRODUCTOS WHERE SECCIÓN = "CERÁMICA" ORDER BY PRECIO DESC;  /*EN LISTA*/

SELECT MAX(PRECIO) FROM PRODUCTOS WHERE SECCIÓN = "CERÁMICA"; /* SACANDO EL MAYOR */

SELECT NOMBREARTÍCULO,SECCIÓN,PRECIO FROM PRODUCTOS WHERE PRECIO > (SELECT MAX(PRECIO) FROM PRODUCTOS WHERE SECCIÓN = "CERÁMICA") ORDER BY PRECIO;

