/* INNER JOIN */


SELECT *
FROM CLIENTES INNER JOIN PEDIDOS
ON CLIENTES.CÓDIGOCLIENTE=PEDIDOS.CÓDIGOCLIENTE
WHERE POBLACIÓN='MADRID';

/* SELECCIONAR TODOS LOS CAMPOS DE LA TABLA CLIENTES DONDE COINCIDAN LOS PEDIDOS DE CLEINTE Y PEDIDO DONDE LA POBLACION SEA IGUAL A MADRID */

